def _build_request(args: argparse.Namespace) -> tuple[MazeRequest, int]:
    """Translate CLI args into a (request, cell_size) pair."""
    maze_type = MazeType(args.maze_type.capitalize())
    cell_size, default_w, default_h = _DEFAULTS[maze_type]
    width = args.width if args.width is not None else default_w
    height = args.height if args.height is not None else default_h
    algorithm = Algorithm(args.algo)
    goal_x, goal_y = width - 1, height - 1
    if maze_type == MazeType.RHOMBIC and (goal_x + goal_y) % 2 != 0:
        # Only (x+y)%2==0 cells exist; adjust goal by one step.
        goal_x = max(0, goal_x - 1)
    request = MazeRequest(
        maze_type=maze_type,
        width=width,
        height=height,
        algorithm=algorithm,
        capture_steps=False,
        start=Coord(x=0, y=0),
        goal=Coord(x=goal_x, y=goal_y),
    )
    return request, cell_size


def _window_size(request: MazeRequest, cell_size: int) -> tuple[int, int]:
    """Compute the window size for a request without touching pygame.

    Orthogonal: width = W*cell, height = H*cell + HUD.
    Sigma: hex bounding box from the iOS reference layout, plus HUD.
    Delta: triangle bounding box (cell_size*(cols+1)/2 × tri_height*rows), plus HUD.
    Rhombic: diamond bounding box from RhombicMazeView (half_diagonal*max_x + diagonal),
      plus HUD.  max_x = width-1, max_y = height-1.
    """
    if request.maze_type == MazeType.ORTHOGONAL:
        return (request.width * cell_size, request.height * cell_size + HUD_HEIGHT)
    if request.maze_type == MazeType.SIGMA:
        hex_height = math.sqrt(3) * cell_size
        w = int(round(cell_size * (1.5 * request.width + 0.5)))
        h = int(round(hex_height * (request.height + 0.5))) + HUD_HEIGHT
        return (w, h)
    if request.maze_type == MazeType.DELTA:
        tri_height = math.sqrt(3) / 2 * cell_size
        w = int(round(cell_size * (request.width + 1) / 2))
        h = int(round(tri_height * request.height)) + HUD_HEIGHT
        return (w, h)
    if request.maze_type == MazeType.RHOMBIC:
        diagonal = cell_size * math.sqrt(2)
        half_diagonal = diagonal / 2
        w = int(round(half_diagonal * (request.width - 1) + diagonal))
        h = int(round(half_diagonal * (request.height - 1) + diagonal)) + HUD_HEIGHT
        return (w, h)
    if request.maze_type == MazeType.UPSILON:
        step = cell_size * math.sqrt(2) / 2
        w = int(round((request.width - 1) * step + cell_size))
        h = int(round((request.height - 1) * step + cell_size)) + HUD_HEIGHT
        return (w, h)
    raise NotImplementedError(f"window sizing for {request.maze_type.value}")


def _max_grid_for_screen(
    maze_type: MazeType, cell_size: int, avail_w: int, avail_h: int
) -> tuple[int, int]:
    """Return (max_cols, max_rows) whose window fits inside avail_w × avail_h.

    Inverts each branch of ``_window_size`` analytically.  Results are clamped
    to at least 2 so a tiny display never produces an invalid request.
    """
    maze_h = max(1, avail_h - HUD_HEIGHT)
    if maze_type == MazeType.ORTHOGONAL:
        return (
            max(2, avail_w // cell_size),
            max(2, maze_h // cell_size),
        )
    if maze_type == MazeType.SIGMA:
        return (
            max(2, int((avail_w / cell_size - 0.5) / 1.5)),
            max(2, int(maze_h / (math.sqrt(3) * cell_size) - 0.5)),
        )
    if maze_type == MazeType.DELTA:
        return (
            max(2, int(avail_w * 2 / cell_size) - 1),
            max(2, int(maze_h / (math.sqrt(3) / 2 * cell_size))),
        )
    if maze_type == MazeType.RHOMBIC:
        diag = cell_size * math.sqrt(2)
        half = diag / 2
        return (
            max(2, int((avail_w - diag) / half) + 1),
            max(2, int((maze_h - diag) / half) + 1),
        )
    if maze_type == MazeType.UPSILON:
        step = cell_size * math.sqrt(2) / 2
        return (
            max(2, int((avail_w - cell_size) / step) + 1),
            max(2, int((maze_h - cell_size) / step) + 1),
        )
    return (40, 40)  # unreachable guard


def _is_solved(cells) -> bool:
    return any(c.is_active and c.is_goal for c in cells)


def _draw_hud(
    surface: pygame.Surface,
    font: pygame.font.Font,
    request: MazeRequest,
    show_heatmap: bool,
    show_solution: bool,
    solved: bool,
    animate_mode: bool = False,
    anim_info: str | None = None,
    hud_msg: str = "",
) -> None:
    pygame.draw.rect(surface, HUD_BG, pygame.Rect(0, 0, surface.get_width(), HUD_HEIGHT))
    title = f"{request.maze_type.value} · {request.algorithm.value}  {request.width}×{request.height}"
    move_hint = _HUD_HINT_BY_TYPE.get(request.maze_type, "")
    if hud_msg:
        hint_text = hud_msg
        hint_color = (220, 120, 20)  # orange — transient warning
    elif anim_info is not None:
        hint_text = anim_info
        hint_color = HUD_HINT_COLOR
    else:
        hint_text = (
            f"H heatmap:{'on' if show_heatmap else 'off'}    "
            f"S solution:{'on' if show_solution else 'off'}    "
            f"R regen    G anim:{'on' if animate_mode else 'off'}    M settings    {move_hint}"
        )
        hint_color = HUD_HINT_COLOR
    surface.blit(font.render(title, True, HUD_TITLE_COLOR), (12, 8))
    surface.blit(font.render(hint_text, True, hint_color), (12, 30))
    if solved:
        msg = font.render("Solved!", True, (16, 130, 60))
        surface.blit(msg, msg.get_rect(midright=(surface.get_width() - 12, HUD_HEIGHT // 2)))


def _draw_solved_overlay(
    surface: pygame.Surface,
    rect: pygame.Rect,
    big_font: pygame.font.Font,
    hint_font: pygame.font.Font,
) -> None:
    overlay = pygame.Surface(rect.size, pygame.SRCALPHA)
    overlay.fill(SOLVED_OVERLAY_RGBA)
    surface.blit(overlay, rect.topleft)
    text = big_font.render("Solved!", True, SOLVED_TEXT_COLOR)
    text_rect = text.get_rect(center=rect.center)
    surface.blit(text, text_rect)
    hint = hint_font.render("Press R / Space / Enter for a new maze", True, SOLVED_TEXT_COLOR)
    surface.blit(hint, hint.get_rect(midtop=(rect.centerx, text_rect.bottom + 8)))


def _apply_new_request(
    new_request: MazeRequest,
    old_maze: Maze,
    screen: pygame.Surface,
) -> tuple[Maze, object, int, pygame.Surface]:
    """Create a new Maze + Renderer for *new_request*.

    Resizes the display window when the new dimensions differ from the
    current window.  Returns ``(maze, renderer, cell_size, screen)``; the
    returned *screen* may be a new surface if the window was resized.

    Raises ``MazeGenerationError`` on incompatible type/algorithm combos so
    the caller can surface the error back to the menu without crashing.
    """
    cell_size, _, _ = _DEFAULTS.get(new_request.maze_type, (28, 0, 0))
    new_size = _window_size(new_request, cell_size)
    if new_size != screen.get_size():
        screen = pygame.display.set_mode(new_size)
    new_maze = Maze(new_request)  # raises MazeGenerationError if incompatible
    old_maze.close()
    renderer = make_renderer(new_request.maze_type, screen, cell_size, offset=(0, HUD_HEIGHT))
    return new_maze, renderer, cell_size, screen


def main(argv: list[str] | None = None) -> None:
    args = _parse_args(argv)
    request, cell_size = _build_request(args)
    key_map = _KEYS_BY_TYPE[request.maze_type]

    pygame.init()
    pygame.key.set_repeat(_GAMEPLAY_REPEAT_DELAY, _GAMEPLAY_REPEAT_INTERVAL)

    # Detect primary display dimensions before the first set_mode call so we
    # can cap grid sizes to what actually fits on screen.
    try:
        _desktop = pygame.display.get_desktop_sizes()
        _sw, _sh = _desktop[0] if _desktop else (1280, 800)
    except Exception:
        _info = pygame.display.Info()
        _sw, _sh = (_info.current_w or 1280), (_info.current_h or 800)
    _avail_w = int(_sw * SCREEN_MARGIN_W)
    _avail_h = int(_sh * SCREEN_MARGIN_H)
    screen_max_sizes: dict[MazeType, tuple[int, int]] = {
        mt: _max_grid_for_screen(mt, _DEFAULTS[mt][0], _avail_w, _avail_h)
        for mt in MazeType
    }

    # Clamp the initial request if CLI args exceed the screen-safe dimensions.
    _smx, _smy = screen_max_sizes[request.maze_type]
    if request.width > _smx or request.height > _smy:
        _cw, _ch = min(request.width, _smx), min(request.height, _smy)
        print(
            f"Warning: {request.width}×{request.height} exceeds screen limit for "
            f"{request.maze_type.value}; clamped to {_cw}×{_ch}",
            file=sys.stderr,
        )
        _goal_x = min(_cw - 1, request.goal.x)
        _goal_y = min(_ch - 1, request.goal.y)
        if request.maze_type == MazeType.RHOMBIC and (_goal_x + _goal_y) % 2 != 0:
            _goal_x = max(0, _goal_x - 1)
        request = dataclasses.replace(
            request, width=_cw, height=_ch, goal=Coord(x=_goal_x, y=_goal_y)
        )

    pygame.display.set_caption(
        f"Mazer — {request.maze_type.value} · {request.algorithm.value} {request.width}×{request.height}"
    )

    screen = pygame.display.set_mode(_window_size(request, cell_size))
    clock = pygame.time.Clock()
    hud_font = pygame.font.SysFont(None, 22)
    big_font = pygame.font.SysFont(None, 72)

    maze = Maze(request)
    renderer = make_renderer(request.maze_type, screen, cell_size, offset=(0, HUD_HEIGHT))
    gradient = generate_gradient()
    renderer.set_gradient(gradient)
    show_heatmap = False
    show_solution = False
    # Once a chord fires from a multi-arrow KEYDOWN, mark every held arrow
    # as consumed so the *other* arrow's eventual KEYDOWN doesn't fire a
    # second move. Cleared on the matching KEYUP.
    arrows_consumed: set[int] = set()
    drag = _DragState()
    # Track which keys are physically held so we can distinguish a fresh
    # KEYDOWN from a repeat KEYDOWN generated by pygame.key.set_repeat.
    # Non-movement keys (H/S/R/M) only respond to fresh presses.
    keys_held: set[int] = set()
    # When the player is on the Solved! screen and holds Space/Enter, the
    # whole screen turns grayscale until release, then a new maze generates.
    solved_btn_pressed: bool = False
    solved: bool = False  # pre-initialised so the event loop can read it on frame 0
    trail: deque = deque(maxlen=3)   # coords of the last 3 vacated cells, index 0 = most recent
    _prev_active_coord = None        # active coord from the previous frame
    _visited_set: set = set()        # all coords the player has ever stood on this maze

    # Sticky animation mode — toggled by G key.  When True, R/N and first
    # launch with --animate generate with capture_steps=True and play the
    # step-by-step animation before handing off to interactive play.
    animate_mode: bool = args.animate
    anim: AnimationState | None = None
    anim_maze: Maze | None = None  # new maze being animated (kept separate for cancel)
    solution_anim: SolutionAnimState | None = None

    menu_state: MenuState | None = None
    menu_layout: MenuLayout | None = None
    # Window size saved just before the menu opens; restored if the user
    # cancels.  None when the menu is not open (or when the window was
    # already large enough and no resize was needed).
    pre_menu_size: tuple[int, int] | None = None
    # Hold-to-scroll state for menu arrow buttons.  Records which arrow
    # (section + delta) is held and when the hold started / last fired.
    _arrow_held: tuple[int, int] | None = None   # (section, delta: -1 or +1)
    _arrow_held_since: int = 0                   # ticks of initial press
    _arrow_last_fire: int = 0                    # ticks of most recent repeat

    # Transient HUD message shown for a fixed duration (e.g. animation-skipped warning).
    _hud_msg: str = ""
    _hud_msg_until: int = 0  # pygame.time.get_ticks() expiry

    def _set_hud_msg(msg: str, duration_ms: int = 4000) -> None:
        nonlocal _hud_msg, _hud_msg_until
        _hud_msg = msg
        _hud_msg_until = pygame.time.get_ticks() + duration_ms

    def _commit_menu_result(open_: bool, new_request) -> None:
        """Apply a menu result to the running game state.

        Mutates the enclosing ``main`` scope via ``nonlocal``.
        """
        nonlocal maze, renderer, cell_size, screen, request, key_map, gradient
        nonlocal menu_state, menu_layout, pre_menu_size, trail, _prev_active_coord
        if not open_:
            if new_request is not None:
                try:
                    maze, renderer, cell_size, screen = _apply_new_request(
                        new_request, maze, screen
                    )
                    request = new_request
                    key_map = _KEYS_BY_TYPE.get(request.maze_type, ORTHOGONAL_KEYS)
                    gradient = generate_gradient()
                    renderer.set_gradient(gradient)
                    trail.clear()
                    _prev_active_coord = None
                    _visited_set.clear()
                    arrows_consumed.clear()
                    drag.end()
                    pygame.display.set_caption(
                        f"Mazer — {request.maze_type.value} · "
                        f"{request.algorithm.value} {request.width}×{request.height}"
                    )
                    if animate_mode:
                        _begin_animation()
                except MazeGenerationError:
                    menu_state.set_generation_error()
                    open_ = True
            if not open_:
                # Menu is truly closing.  If we expanded the window to fit
                # the menu panel and the user cancelled (no new maze was
                # applied), shrink back to the original game window size.
                if new_request is None and pre_menu_size is not None:
                    if screen.get_size() != pre_menu_size:
                        screen = pygame.display.set_mode(pre_menu_size)
                        renderer = make_renderer(
                            request.maze_type, screen, cell_size, offset=(0, HUD_HEIGHT)
                        )
                        renderer.set_gradient(gradient)
                pre_menu_size = None
                keys_held.clear()
                pygame.key.set_repeat(_GAMEPLAY_REPEAT_DELAY, _GAMEPLAY_REPEAT_INTERVAL)
                menu_state = None
                menu_layout = None

    def _begin_animation() -> None:
        """Start step-by-step animation for the current request.

        Returns immediately (no animation) if the grid exceeds ANIM_MAX_W ×
        ANIM_MAX_H — the menu already prevents this for in-game sessions; this
        guard catches the CLI ``--animate`` path on an oversized grid.
        """
        nonlocal anim, anim_maze, solved_btn_pressed, show_solution, solution_anim
        if request.width > ANIM_MAX_W or request.height > ANIM_MAX_H:
            _set_hud_msg(
                f"⚠ Too large to animate ({request.width}×{request.height}"
                f" > {ANIM_MAX_W}×{ANIM_MAX_H})"
                f" — reduce size (M) or disable anim (G)"
            )
            return
        if anim_maze is not None:
            anim_maze.close()
        anim_request = dataclasses.replace(request, capture_steps=True)
        anim_maze = Maze(anim_request)
        all_steps = list(anim_maze.generation_steps())
        anim = AnimationState(all_steps)
        show_solution = False
        solution_anim = None
        renderer.set_gradient(None)
        drag.end()
        arrows_consumed.clear()
        solved_btn_pressed = False

    def _complete_animation() -> None:
        """Finish animation (normal end or skip) — switch to interactive play."""
        nonlocal maze, anim, anim_maze, gradient, trail, _prev_active_coord, show_solution, solution_anim
        if anim is not None:
            anim.skip()
        maze.close()
        maze = anim_maze
        anim_maze = None
        anim = None
        show_solution = False
        solution_anim = None
        trail.clear()
        _prev_active_coord = None
        _visited_set.clear()
        gradient = generate_gradient(gradient.base)
        renderer.set_gradient(gradient)

    def _cancel_animation() -> None:
        """Esc during animation — discard new maze, restore old interactive maze."""
        nonlocal anim, anim_maze
        if anim_maze is not None:
            anim_maze.close()
        anim_maze = None
        anim = None
        renderer.set_gradient(gradient)

    if args.animate:
        _begin_animation()

    dt: int = 0  # milliseconds since last frame; updated at end of each loop iteration

    try:
        running = True
        while running:
            for event in pygame.event.get():
                if event.type == pygame.QUIT:
                    running = False

                # ---- Menu open: route all input to the menu ----------------
                elif menu_state is not None:
                    if event.type == pygame.KEYDOWN:
                        open_, new_request = menu_state.handle_keydown(event.key)
                        _commit_menu_result(open_, new_request)
                    elif event.type == pygame.KEYUP:
                        open_, new_request = menu_state.handle_keyup(event.key)
                        _commit_menu_result(open_, new_request)
                    elif event.type == pygame.MOUSEBUTTONDOWN and event.button == 1:
                        if menu_layout is not None:
                            # Detect arrow-button hold for auto-repeat.
                            _arrow_held = None
                            now = pygame.time.get_ticks()
                            for _sec, _rect in menu_layout.left_arrows.items():
                                if _rect.collidepoint(event.pos):
                                    _arrow_held = (_sec, -1)
                                    _arrow_held_since = now
                                    _arrow_last_fire = now
                                    break
                            if _arrow_held is None:
                                for _sec, _rect in menu_layout.right_arrows.items():
                                    if _rect.collidepoint(event.pos):
                                        _arrow_held = (_sec, +1)
                                        _arrow_held_since = now
                                        _arrow_last_fire = now
                                        break
                            open_, new_request = menu_state.handle_mousedown(event.pos, menu_layout)
                            _commit_menu_result(open_, new_request)
                    elif event.type == pygame.MOUSEBUTTONUP and event.button == 1:
                        _arrow_held = None
                        if menu_layout is not None:
                            open_, new_request = menu_state.handle_mouseup(event.pos, menu_layout)
                            _commit_menu_result(open_, new_request)

                # ---- Animation in progress ---------------------------------
                elif anim is not None:
                    if event.type == pygame.KEYDOWN:
                        if event.key == pygame.K_ESCAPE:
                            _cancel_animation()
                        elif event.key in (pygame.K_SPACE, pygame.K_RETURN, pygame.K_KP_ENTER):
                            _complete_animation()
                        elif event.key == pygame.K_g:
                            animate_mode = not animate_mode
                        elif event.key == pygame.K_h:
                            show_heatmap = not show_heatmap
                        elif event.key == pygame.K_s:
                            show_solution = not show_solution
                    elif event.type == pygame.MOUSEBUTTONDOWN and event.button == 1:
                        _complete_animation()

                # ---- Normal gameplay ----------------------------------------
                elif event.type == pygame.KEYDOWN:
                    is_repeat = event.key in keys_held
                    keys_held.add(event.key)
                    if event.key == pygame.K_ESCAPE:
                        if not is_repeat:
                            running = False
                    elif event.key in (pygame.K_SPACE, pygame.K_RETURN, pygame.K_KP_ENTER, pygame.K_n):
                        if not is_repeat and (solved or event.key in (pygame.K_SPACE, pygame.K_n)):
                            solved_btn_pressed = True
                    elif event.key == pygame.K_m:
                        if not is_repeat:
                            solved_btn_pressed = False
                            menu_state = MenuState(
                                request,
                                max_sizes=screen_max_sizes,
                                animate_mode=animate_mode,
                                anim_max_w=ANIM_MAX_W,
                                anim_max_h=ANIM_MAX_H,
                            )
                            menu_layout = None
                            keys_held.clear()
                            pygame.key.set_repeat(300, 60)
                            # Enlarge the window if it's too small to show the
                            # full menu panel, and remember the original size so
                            # we can restore it if the user cancels.
                            pre_menu_size = screen.get_size()
                            need_w = max(screen.get_width(), _MENU_MIN_WINDOW_W)
                            need_h = max(screen.get_height(), _MENU_MIN_WINDOW_H)
                            if (need_w, need_h) != pre_menu_size:
                                screen = pygame.display.set_mode((need_w, need_h))
                            else:
                                pre_menu_size = None  # no resize — nothing to restore
                    elif event.key == pygame.K_g:
                        if not is_repeat:
                            animate_mode = not animate_mode
                    elif event.key == pygame.K_h:
                        if not is_repeat:
                            show_heatmap = not show_heatmap
                    elif event.key == pygame.K_s:
                        if not is_repeat:
                            show_solution = not show_solution
                            if show_solution and anim is None:
                                solution_cells = [c for c in maze.cells() if c.on_solution_path]
                                solution_anim = SolutionAnimState(solution_cells)
                            else:
                                solution_anim = None
                    elif event.key == pygame.K_r:
                        if not is_repeat:
                            solved_btn_pressed = False
                            show_solution = False
                            solution_anim = None
                            if animate_mode:
                                _begin_animation()
                            else:
                                maze.close()
                                maze = Maze(request)
                                gradient = generate_gradient(gradient.base)
                                renderer.set_gradient(gradient)
                                arrows_consumed.clear()
                                drag.end()
                    elif event.key in ARROW_KEYS:
                        if event.key in arrows_consumed:
                            continue
                        keys = pygame.key.get_pressed()
                        direction = _resolve_chord(
                            keys[pygame.K_UP],
                            keys[pygame.K_DOWN],
                            keys[pygame.K_LEFT],
                            keys[pygame.K_RIGHT],
                        )
                        if direction is not None:
                            # Rhombic: open_walls stores diagonal names directly.
                            # Single-arrow cardinals (UP/DOWN/LEFT/RIGHT) don't
                            # match open_walls — remap to the nearest diagonal so
                            # ↑ → UpperRight, → → LowerRight, ↓ → LowerLeft,
                            # ← → UpperLeft.  Diagonal chords (↑+→ etc.) are
                            # already valid and pass through unchanged.
                            if (
                                request.maze_type == MazeType.RHOMBIC
                                and direction not in _RHOMBIC_VALID_DIRECTIONS
                            ):
                                direction = _RHOMBIC_CARDINAL_TO_DIAGONAL[direction]
                            # Delta mazes have no native Left/Right neighbors.
                            # Map the cardinal to the exact horizontal diagonal
                            # for the active cell's orientation so hold-to-move
                            # works reliably (avoids the Rust fallback stalling
                            # on the wrong diagonal after every alternating cell).
                            if (
                                request.maze_type == MazeType.DELTA
                                and direction in (Direction.LEFT, Direction.RIGHT)
                            ):
                                direction = _delta_horizontal(direction, maze.cells())
                            _move_with_slide(maze, direction, request.maze_type)
                            held_arrows = [k for k in ARROW_KEYS if keys[k]]
                            if len(held_arrows) > 1:
                                arrows_consumed.update(held_arrows)
                    elif event.key in key_map:
                        _move_with_slide(maze, key_map[event.key], request.maze_type)
                elif event.type == pygame.KEYUP:
                    keys_held.discard(event.key)
                    if event.key in ARROW_KEYS:
                        arrows_consumed.discard(event.key)
                    if (
                        event.key in (pygame.K_SPACE, pygame.K_RETURN, pygame.K_KP_ENTER, pygame.K_n)
                        and solved_btn_pressed
                    ):
                        solved_btn_pressed = False
                        if animate_mode:
                            _begin_animation()
                        else:
                            maze.close()
                            maze = Maze(request)
                            trail.clear()
                            _prev_active_coord = None
                            _visited_set.clear()
                            gradient = generate_gradient(gradient.base)
                            renderer.set_gradient(gradient)
                            arrows_consumed.clear()
                            drag.end()
                elif event.type == pygame.MOUSEBUTTONDOWN and event.button == 1:
                    if solved:
                        solved_btn_pressed = False
                        if animate_mode:
                            _begin_animation()
                        else:
                            maze.close()
                            maze = Maze(request)
                            trail.clear()
                            _prev_active_coord = None
                            _visited_set.clear()
                            gradient = generate_gradient(gradient.base)
                            renderer.set_gradient(gradient)
                            arrows_consumed.clear()
                            drag.end()
                    else:
                        drag.begin(event.pos)
                elif event.type == pygame.MOUSEMOTION:
                    drag.motion(event.pos, maze, cell_size, request.maze_type)
                elif event.type == pygame.MOUSEBUTTONUP and event.button == 1:
                    drag.end()

            # Hold-to-scroll: fire repeated value changes while an arrow button
            # is held, mirroring pygame's key-repeat behaviour for the menu.
            if menu_state is not None and _arrow_held is not None and menu_layout is not None:
                _now = pygame.time.get_ticks()
                if _now - _arrow_held_since >= _MENU_ARROW_REPEAT_DELAY:
                    if _now - _arrow_last_fire >= _MENU_ARROW_REPEAT_INTERVAL:
                        _held_sec, _held_delta = _arrow_held
                        _arrow_map = (
                            menu_layout.left_arrows if _held_delta == -1
                            else menu_layout.right_arrows
                        )
                        if (
                            _held_sec in _arrow_map
                            and _arrow_map[_held_sec].collidepoint(pygame.mouse.get_pos())
                        ):
                            menu_state.section = _held_sec
                            menu_state._change_value(_held_delta)
                        _arrow_last_fire = _now

            # Advance animation with this frame's tick delta.
            if anim is not None:
                if anim.tick(min(dt, 32)):
                    _complete_animation()

            if solution_anim is not None:
                solution_anim.tick(min(dt, 32))

            if anim is not None and anim.steps:
                cells = anim.steps[anim.current_step]
                solved = False
                active_trail = None
            else:
                cells = maze.cells()
                solved = _is_solved(cells)
                active_coord = next((c.coord for c in cells if c.is_active), None)
                if active_coord != _prev_active_coord and _prev_active_coord is not None:
                    if active_coord not in _visited_set:  # new cell — moving forward
                        trail.appendleft(_prev_active_coord)
                    else:                                 # already visited — backtracking
                        trail.clear()
                if active_coord is not None:
                    _visited_set.add(active_coord)
                _prev_active_coord = active_coord
                active_trail = list(trail) if trail else None

            solution_revealed: frozenset | None = (
                solution_anim.revealed_set
                if solution_anim is not None and not solution_anim.done
                else None
            )

            screen.fill((20, 20, 24))
            renderer.draw(cells, show_heatmap=show_heatmap, show_solution=show_solution, trail=active_trail, solution_revealed=solution_revealed)
            if solved:
                _draw_solved_overlay(screen, renderer.maze_rect(cells), big_font, hud_font)

            anim_info: str | None = None
            if anim is not None:
                step = anim.current_step + 1
                total = len(anim.steps)
                anim_info = f"Animating {step}/{total} — Space/click to skip   Esc cancel"
            # Expire transient HUD message if its timer has elapsed.
            _active_hud_msg = _hud_msg if pygame.time.get_ticks() < _hud_msg_until else ""
            _draw_hud(
                screen, hud_font, request, show_heatmap, show_solution, solved,
                animate_mode=animate_mode, anim_info=anim_info, hud_msg=_active_hud_msg,
            )
            if menu_state is not None:
                menu_layout = draw_menu(screen, menu_state, hud_font)

            if solved_btn_pressed:
                gray = pygame.transform.grayscale(screen)
                screen.blit(gray, (0, 0))

            pygame.display.flip()
            dt = clock.tick(60)
    finally:
        maze.close()
        if anim_maze is not None:
            anim_maze.close()
        pygame.quit()


if __name__ == "__main__":
    main()
