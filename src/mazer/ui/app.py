# Adjacent directions (±45° on the 8-direction wheel) to try when the
# primary direction is walled off (orthogonal and sigma).  Delta uses a
# separate orientation-aware vertical slide — see _move_with_slide.
_SLIDE_ALTS: dict[Direction, tuple[Direction, Direction]] = {
    Direction.UP:          (Direction.UPPER_LEFT,  Direction.UPPER_RIGHT),
    Direction.UPPER_RIGHT: (Direction.UP,          Direction.RIGHT),
    Direction.RIGHT:       (Direction.UPPER_RIGHT, Direction.LOWER_RIGHT),
    Direction.LOWER_RIGHT: (Direction.RIGHT,       Direction.DOWN),
    Direction.DOWN:        (Direction.LOWER_RIGHT, Direction.LOWER_LEFT),
    Direction.LOWER_LEFT:  (Direction.DOWN,        Direction.LEFT),
    Direction.LEFT:        (Direction.LOWER_LEFT,  Direction.UPPER_LEFT),
    Direction.UPPER_LEFT:  (Direction.LEFT,        Direction.UP),
}

# Directions that represent horizontal movement in a delta maze (produced
# by _delta_horizontal converting a LEFT/RIGHT arrow to the orientation-
# correct diagonal).
_DELTA_HORIZONTAL_DIRS = frozenset({
    Direction.UPPER_LEFT, Direction.UPPER_RIGHT,
    Direction.LOWER_LEFT, Direction.LOWER_RIGHT,
})


def _move_with_slide(maze, direction: Direction, maze_type: MazeType) -> bool:
    """Fire *direction*; if blocked, try a slide alternative.

    Rhombic: no slide alts — only four diagonal directions exist and the
    Rust's built-in fallback already handles any secondary attempt.  Sending
    a cardinal slide alt (e.g. RIGHT as alt for UPPER_RIGHT) would cause the
    Rust to fall back to LOWER_RIGHT, which is a valid Rhombic direction but
    in the opposite vertical, producing unexpected moves.

    Delta horizontal slide is orientation-aware: when UPPER/LOWER_RIGHT/LEFT
    is blocked, slide to the one vertical neighbor that genuinely exists for
    the active cell's orientation — Normal cells connect downward only,
    Inverted cells connect upward only.  Using the wrong vertical would hit
    the Rust's diagonal fallback and land on the *opposite* horizontal
    neighbour (the cell just left), causing back-and-forth oscillation.

    All other maze types use the ±45° direction-wheel table (_SLIDE_ALTS).
    """
    if maze.move(direction):
        return True
    if maze_type == MazeType.RHOMBIC:
        return False
    if maze_type == MazeType.DELTA and direction in _DELTA_HORIZONTAL_DIRS:
        cells = maze.cells()
        active = next((c for c in cells if c.is_active), None)
        if active is not None:
            vertical = (
                Direction.DOWN if active.orientation.lower() == "normal"
                else Direction.UP
            )
            return maze.move(vertical)
        return False
    for alt in _SLIDE_ALTS.get(direction, ()):
        if maze.move(alt):
            return True
    return False


def _delta_horizontal(direction: Direction, cells: list) -> Direction:
    """Map LEFT/RIGHT to the orientation-correct diagonal for the active delta cell.

    Normal cells (apex up) have UpperLeft/UpperRight as horizontal neighbors.
    Inverted cells (apex down) have LowerLeft/LowerRight.  Sending the exact
    diagonal skips the Rust fallback's intermediate attempt at the wrong
    diagonal (which has no neighbor on the current cell), preventing the
    one-cell-then-stuck behaviour when holding LEFT or RIGHT in a delta maze.
    """
    active = next((c for c in cells if c.is_active), None)
    if active is None:
        return direction
    is_normal = active.orientation.lower() == "normal"
    if direction == Direction.LEFT:
        return Direction.UPPER_LEFT if is_normal else Direction.LOWER_LEFT
    if direction == Direction.RIGHT:
        return Direction.UPPER_RIGHT if is_normal else Direction.LOWER_RIGHT
    return direction


def _resolve_chord(up: bool, down: bool, left: bool, right: bool) -> Direction | None:
    """Resolve held arrow keys to a single Direction.

    Diagonal combinations win over cardinals. Opposing pairs (UP+DOWN,
    LEFT+RIGHT) cancel out — they're treated as if the perpendicular axis
    isn't held, so e.g. UP+DOWN+RIGHT resolves to RIGHT.

    Returns ``None`` if no arrow is held (callers shouldn't reach this
    case on KEYDOWN — defensive only).
    """
    vertical = up ^ down  # cancel if both
    horizontal = left ^ right
    if vertical and horizontal:
        if up and right:
            return Direction.UPPER_RIGHT
        if up and left:
            return Direction.UPPER_LEFT
        if down and right:
            return Direction.LOWER_RIGHT
        if down and left:
            return Direction.LOWER_LEFT
    if vertical:
        return Direction.UP if up else Direction.DOWN
    if horizontal:
        return Direction.LEFT if left else Direction.RIGHT
    return None


# Orthogonal: eight 45° sectors.  Angle 0° = rightward (+x), clockwise
# (y-down screen coords).  Each sector boundary is at multiples of 45°
# offset by 22.5° so the cardinal and diagonal centres are equidistant.
# Diagonals are passed straight to the Rust ``make_move`` which tries the
# vertical component first, then the horizontal — e.g. UPPER_RIGHT tries
# Up → Right — giving the liberal fallback the user feels on the trackpad.
_ORTHO_SECTOR_DIRECTIONS = (
    Direction.RIGHT,        # sector 0: 337.5°–22.5°
    Direction.LOWER_RIGHT,  # sector 1: 22.5°–67.5°
    Direction.DOWN,         # sector 2: 67.5°–112.5°
    Direction.LOWER_LEFT,   # sector 3: 112.5°–157.5°
    Direction.LEFT,         # sector 4: 157.5°–202.5°
    Direction.UPPER_LEFT,   # sector 5: 202.5°–247.5°
    Direction.UP,           # sector 6: 247.5°–292.5°
    Direction.UPPER_RIGHT,  # sector 7: 292.5°–337.5°
)

# Sigma: six 60° sectors mapped to hex directions, indexed by
# ``int((angle_degrees + 30) % 360 / 60)``.  Angle 0° = rightward (screen
# x+), increasing clockwise (y-down screen coords).  Centers per sector:
# 330° UPPER_RIGHT, 30° LOWER_RIGHT, 90° DOWN, 150° LOWER_LEFT,
# 210° UPPER_LEFT, 270° UP.
_SIGMA_SECTOR_DIRECTIONS = (
    Direction.UPPER_RIGHT,  # sector 0: 300°–360° / 0°–30°
    Direction.LOWER_RIGHT,  # sector 1: 30°–90°
    Direction.DOWN,         # sector 2: 90°–150°
    Direction.LOWER_LEFT,   # sector 3: 150°–210°
    Direction.UPPER_LEFT,   # sector 4: 210°–270°
    Direction.UP,           # sector 5: 270°–330°
)

# Rhombic: four 90° sectors, boundaries at the cardinal axes.
# Angle 0° = rightward (screen x+), clockwise.  Sector = int(angle / 90) % 4.
#   0°–90°   → LOWER_RIGHT  (center 45°)
#   90°–180° → LOWER_LEFT   (center 135°)
#   180°–270°→ UPPER_LEFT   (center 225°)
#   270°–360°→ UPPER_RIGHT  (center 315°)
_RHOMBIC_SECTOR_DIRECTIONS = (
    Direction.LOWER_RIGHT,  # sector 0: 0°–90°
    Direction.LOWER_LEFT,   # sector 1: 90°–180°
    Direction.UPPER_LEFT,   # sector 2: 180°–270°
    Direction.UPPER_RIGHT,  # sector 3: 270°–360°
)


def _direction_from_vector(dx: float, dy: float, maze_type: MazeType) -> Direction | None:
    """Resolve a drag vector to a Direction based on maze type.

    Orthogonal/Delta: angle mapped to nearest of 8 directions (45° sectors).
      Diagonal directions are passed to the Rust which applies per-type
      fallback — e.g. UPPER_RIGHT on a Normal delta cell tries UpperRight
      directly; on an Inverted cell the Rust resolves it to Up or Right.
    Sigma: angle mapped to nearest of 6 hex directions (60° sectors).
    Rhombic: angle mapped to nearest of 4 diagonal directions (90° sectors).
    Returns None for zero-length vectors or unimplemented maze types.
    """
    if dx == 0 and dy == 0:
        return None
    angle = (math.degrees(math.atan2(dy, dx)) + 360) % 360
    if maze_type in (MazeType.ORTHOGONAL, MazeType.DELTA, MazeType.UPSILON):
        sector = int((angle + 22.5) / 45) % 8
        return _ORTHO_SECTOR_DIRECTIONS[sector]
    if maze_type == MazeType.SIGMA:
        sector = int((angle + 30) % 360 / 60)
        return _SIGMA_SECTOR_DIRECTIONS[sector]
    if maze_type == MazeType.RHOMBIC:
        sector = int(angle / 90) % 4
        return _RHOMBIC_SECTOR_DIRECTIONS[sector]
    return None


class _DragState:
    """Swipe-to-move state machine.

    Click anywhere on the maze and hold.  As the cursor moves, the drag
    vector from the last-fired position is resolved to a direction; when
    it exceeds the threshold (``cell_size * 0.6``) a move fires and the
    anchor resets to the current position so the next move requires
    another threshold-worth of drag.  Works like a joystick — direction
    of travel determines movement, not which cell the cursor is on.
    """

    def __init__(self) -> None:
        self.active = False
        self._origin: tuple[int, int] | None = None

    def begin(self, pos: tuple[int, int]) -> None:
        """Start a drag session, recording the cursor anchor point."""
        self.active = True
        self._origin = pos

    def motion(
        self,
        pos: tuple[int, int],
        maze,
        cell_size: int,
        maze_type: MazeType,
    ) -> bool:
        """Handle MOUSEMOTION. Fires a move when the accumulated drag exceeds the threshold."""
        if not self.active or self._origin is None:
            return False
        ox, oy = self._origin
        dx = pos[0] - ox
        dy = pos[1] - oy
        if math.hypot(dx, dy) < cell_size * 0.6:
            return False
        direction = _direction_from_vector(dx, dy, maze_type)
        if direction is None:
            return False
        fired = maze.move(direction)
        self._origin = pos
        return fired

    def end(self) -> None:
        self.active = False
        self._origin = None


ANIM_STEP_INTERVAL_MS: float = 15.0


class AnimationState:
    """Step-by-step maze generation animation controller.

    Pre-loads all generation steps; ``tick()`` advances by elapsed wall-clock
    time and returns True when the last step is reached.  ``skip()`` jumps to
    the final frame immediately, idempotent if already done.
    """

    def __init__(self, steps: list) -> None:
        self.steps = steps
        self.current_step: int = 0
        self.done: bool = len(steps) == 0
        self._accum_ms: float = 0.0

    def tick(self, elapsed_ms: float) -> bool:
        """Advance by *elapsed_ms*. Returns True when animation completes."""
        if self.done or not self.steps:
            self.done = True
            return False
        self._accum_ms += elapsed_ms
        steps_to_advance = int(self._accum_ms / ANIM_STEP_INTERVAL_MS)
        if steps_to_advance > 0:
            self._accum_ms -= steps_to_advance * ANIM_STEP_INTERVAL_MS
            self.current_step = min(
                self.current_step + steps_to_advance, len(self.steps) - 1
            )
        if self.current_step >= len(self.steps) - 1:
            self.done = True
            return True
        return False

    def skip(self) -> None:
        """Jump to the final step."""
        if self.steps:
            self.current_step = len(self.steps) - 1
        self.done = True


class SolutionAnimState:
    """Reveal solution path one cell at a time, sorted by distance from start."""

    def __init__(self, solution_cells: list) -> None:
        sorted_cells = sorted(solution_cells, key=lambda c: c.distance)
        self.coords: list = [c.coord for c in sorted_cells]
        self.revealed_count: int = 0
        self.done: bool = len(self.coords) == 0
        self._accum_ms: float = 0.0

    @property
    def revealed_set(self) -> frozenset:
        return frozenset(self.coords[: self.revealed_count])

    def tick(self, elapsed_ms: float) -> bool:
        """Advance by elapsed_ms. Returns True when all cells are revealed."""
        if self.done:
            return False
        self._accum_ms += elapsed_ms
        steps = int(self._accum_ms / ANIM_STEP_INTERVAL_MS)
        if steps > 0:
            self._accum_ms -= steps * ANIM_STEP_INTERVAL_MS
            self.revealed_count = min(self.revealed_count + steps, len(self.coords))
        if self.revealed_count >= len(self.coords):
            self.done = True
            return True
        return False


